#include <stdio.h>

int main() {
    int a, b, product;
    a = 20;
    b = 7;
    //Calculating product
    product = a + b;
    printf("Product = %d \n", product);
    product = a - b;
    printf("Product = %d \n", product);
    product = a * b;
    printf("Product = %d \n", product);
    product = a / b;
    printf("Product = %d \n", product);
    product = a % b;
    printf("Product = %d \n", product);
    return 0;
}
